//=============================================================================
// 等速直線運動
// s＝gt*t/2 = v*t/2
//=============================================================================
int UniformAccelerationsMotion(float *WorldPosition, float *Speed/*右、下はプラス*/)
{
	(*WorldPosition) += 0.5f * (*Speed) * 1/60.0f;

	return 0;
}

//=============================================================================
// 自由落下 -- 等加速度直線運動
// 距離s=(x*x+y*y)1/2
//=============================================================================
int FreeFallMotion(float *WorldPosition, float *Speed, float *Accelerarion)
{
	// 移動処理
	UniformAccelerationsMotion(WorldPosition, Speed);

	// 加速度変化
	(*Speed) += *Accelerarion;

	return 0;
}